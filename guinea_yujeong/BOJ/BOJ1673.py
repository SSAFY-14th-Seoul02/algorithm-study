'''
[문제]
강민이는 치킨 한 마리를 주문할 수 있는 치킨 쿠폰을 n장 가지고 있다. 
이 치킨집에서는 치킨을 한 마리 주문할 때마다 도장을 하나씩 찍어 주는데, 도장을 k개 모으면 치킨 쿠폰 한 장으로 교환할 수 있다.

강민이가 지금 갖고 있는 치킨 쿠폰으로 치킨을 최대 몇 마리나 먹을 수 있는지 구하여라. 
단, 치킨을 주문하기 위해서는 반드시 치킨 쿠폰을 갖고 있어야 한다.

[입력]
여러 줄에 걸쳐서 자연수 n과 k가 주어진다.

[출력]
각 입력마다 한 줄에 정답을 출력한다.
'''

# BOJ 1673: 치킨 쿠폰

while True:
    try:
        n, k = map(int, input().split())  
    except :
        break  # 더 이상 입력이 없으면 종료

    chicken = n     # 처음 산 치킨 개수
    coupons = n     # 처음 받은 쿠폰 수 (치킨 1마리마다 쿠폰 1장)

    # 쿠폰으로 치킨 교환을 끝까지 반복
    while coupons >= k:
        free = coupons // k           # 이번에 교환할 수 있는 추가 치킨 수
        chicken += free                 # 총 치킨 수 증가
        coupons = free + (coupons % k)  # 새 쿠폰 + 안 쓰고 남은 쿠폰

    print(chicken)
